#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int		i;
	char	a;

	i = 0;
	while (s[i])
	{
		a = s[i];
		write(fd, &a, sizeof(char) * 1);
		i++;
	}
}
