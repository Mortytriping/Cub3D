#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		a;
	long int	nb;

	nb = n;
	a = '0';
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = nb * -1;
	}
	if (nb >= 10)
	{
		ft_putnbr_fd((nb / 10), fd);
		ft_putnbr_fd((nb % 10), fd);
	}
	if (nb < 10)
	{
		a += nb;
		write (fd, &a, 1);
	}
}
