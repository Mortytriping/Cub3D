#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*temps;

	temps = s;
	i = 0;
	while (i < n)
	{
		temps[i] = 0;
		i++;
	}
	return ;
}
