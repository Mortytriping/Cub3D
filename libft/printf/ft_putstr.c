#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	int	i;

	if (!s)
	{
		write (1, "(null)", 6);
		(*len) += 6;
		return ;
	}
	i = 0;
	while (s[i])
	{
		write (1, &s[i], 1);
		i++;
		(*len)++;
	}
}
